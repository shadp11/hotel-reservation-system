    private void cashpaytxt2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cashpaytxt2KeyPressed
        String cash1 = cashpaytxt2.getText();
        int cash1_length = cash1.length();
        char b1 = evt.getKeyChar();

        if (Character.isDigit(b1)) {

            if (cash1_length < 20) {
                cashpaytxt2.setEditable(true);
            } else {
                cashpaytxt2.setEditable(false);
            }
        } else {
            if (Character.isISOControl(b1)) {
                cashpaytxt2.setEditable(true);
            } else {
                cashpaytxt2.setEditable(false);
            }
        }
    }//GEN-LAST:event_cashpaytxt2KeyPressed

