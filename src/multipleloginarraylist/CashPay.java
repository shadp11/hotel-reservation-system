    private void cashpaybtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashpaybtn1ActionPerformed
        
        String cashpayamountText = cashpaytxt1.getText();
        String cashpayinText = cashpaytxt2.getText();

        if (cashpayamountText.isEmpty() || cashpayinText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "ENTER ALL REQUIRED FIELDS!");
        } else {
            try {
                int cashpayamount = Integer.parseInt(cashpayamountText);
                int cashpayin = Integer.parseInt(cashpayinText);
                int change = cashpayin - cashpayamount;
                String newchange = Integer.toString(change);

                if (cashpayin < cashpayamount){
                    JOptionPane.showMessageDialog(null, "Money is insufficient");
                }
                else if(cashpayamount < 500){
                    JOptionPane.showMessageDialog(null, "Total amount must be valid");
                }
                else {
                    JOptionPane.showMessageDialog(null, "PAYMENT SUCCESSFUL!");
                    String totalearnings = Dashboardd.totalearnslbl.getText();
                    String amount = cashpayamountText;
                    int totalearningss = Integer.parseInt(totalearnings);
                    int amountt = Integer.parseInt(amount);
                    int totall = totalearningss + amountt;
                    String totalll = Integer.toString(totall);
                    Dashboardd.totalearnslbl.setText(totalll);
                    MultipleLogin.totalearnings.add(totalll);
                    cashpaytxt3.setText(newchange);
                    cashpaytxt1.setText("");
                    cashpaytxt2.setText("");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter valid numbers.");
    }
}
    }//GEN-LAST:event_cashpaybtn1ActionPerformed

